    public void salvar() {
        Admin a = new Admin();
        a.setNome(txtnome.getText());
        a.setSenha(txtSenha.getText());
        a.setConfirmaSenha(txtConfirmaSenha.getText());
        if (txtnome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo em branco", "ERRO", JOptionPane.ERROR_MESSAGE);
            txtnome.requestFocus();
        } else if (txtSenha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo em branco", "ERRO", JOptionPane.ERROR_MESSAGE);
            txtSenha.requestFocus();
        } else if (txtConfirmaSenha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo em branco", "ERRO", JOptionPane.ERROR_MESSAGE);
            txtConfirmaSenha.requestFocus();
        } else if (txtSenha.getText() == null ? txtConfirmaSenha.getText() != null : !txtSenha.getText().equals(txtConfirmaSenha.getText())) {
            JOptionPane.showMessageDialog(null, "A senhas devem ser iguais", "ERRO", JOptionPane.ERROR_MESSAGE);
            txtSenha.requestFocus();
        } else if (txtSenha.getText().length() < 6) {
            JOptionPane.showMessageDialog(null, "A senha esta muito curta,tem que ter no minimo 6 digitos", "ERRO", JOptionPane.ERROR_MESSAGE);
            txtSenha.requestFocus();
        } else if (txtSenha.getText().length() > 12) {
            JOptionPane.showMessageDialog(null, "A senha esta muito grande,tem que ter ate 12 digitos", "ERRO", JOptionPane.ERROR_MESSAGE);
            txtSenha.requestFocus();
        } else {
            entityManagerAdmin.getTransaction().begin();
            entityManagerAdmin.persist(a);
            entityManagerAdmin.getTransaction().commit();
            JOptionPane.showMessageDialog(null, "Cadastrado com sucesso", "Cadastrado", JOptionPane.INFORMATION_MESSAGE);
            list1.add(a);
            limparCampos();
        }
    }

