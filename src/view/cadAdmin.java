    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int ls = jTable1.getSelectedRow();
        if (ls >= 0) {
            Admin a = list1.get(ls);
            entityManagerAdmin.getTransaction().begin();
            entityManagerAdmin.remove(a);
            entityManagerAdmin.getTransaction().commit();
            list1.remove(a);
        } else {
            JOptionPane.showMessageDialog(null, "Selecione na tabela para excluir", "INFORMAÇÂO", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

