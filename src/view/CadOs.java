    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        int ls = jTable1.getSelectedRow();
        if (ls >= 0) {
            Cliente c = list1.get(ls);
            entityManagerOs.getTransaction().begin();
            entityManagerOs.remove(c);
            entityManagerOs.getTransaction().commit();
            list1.remove(c);
        } else {
            JOptionPane.showMessageDialog(null, "Selecione na tabela para excluir", "INFORMAÇÂO", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

