    private void atualizar() {
        int ls = jTable1.getSelectedRow();
        if (ls >= 0) {
            Cliente c = list1.get(ls);
            c.setNome(txtNome.getText());
            c.setSobreNome(txtSobreNome.getText());
            c.setRg(txtRg.getText());
            c.setCpf(txtCpf.getText());
            c.setEndereco(txtEndereco.getText());
            c.setCelular(txtCelular.getText());
            c.setEmail(txtEmail.getText());
            c.setTelefone(txtTelefone.getText());
            c.setOperadoraCel((String) cboOperadoraCli.getSelectedItem());
            c.getOs().setImei(txtImei.getText());
            c.getOs().setModelo(txtModelo.getText());
            c.getOs().setChip((String) cboChip.getSelectedItem());
            c.getOs().setCartao((String) cboCartaoM.getSelectedItem());
            c.getOs().setDefeito(txtDefeito.getText());
            c.getOs().setOperadora((String) cboOperadoraA.getSelectedItem());
            c.getOs().setObservacao(txtObservacao.getText());
            c.getOs().setSenha(txtSenhaApare.getText());
            c.getOs().setTipoServico((String) cboTipoServico.getSelectedItem());
            c.getOs().setDataEntrada(txtDataEntrada.getText());
            c.getOs().setParaDia(txtParaDia.getText());
            c.getOs().setValorServico(txtValorServico.getText());
            c.getOs().setFormaPagamento((String) cboFormaPagamento.getSelectedItem());
            c.getOs().setSinal(txtSinalDeixado.getText());
            c.getOs().setStatus((String) cboStatus.getSelectedItem());
            c.getOs().setEmitente(txtEmitente.getText());
            entityManagerOs.getTransaction().begin();
            entityManagerOs.merge(c);
            entityManagerOs.getTransaction().commit();
            list1.set(ls, c);
        }
    }

